def items_for(vertical: str = ALL) -> list[FaqItem]:
    """Вопросы для выбранного направления. ALL показывает всё."""
    if vertical == ALL:
        return list(ITEMS)
    return [i for i in ITEMS if i.vertical in (ALL, vertical)]
