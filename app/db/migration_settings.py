"""
Параметры выполнения миграций (используются alembic/env.py).

Вынесено отдельным модулем сознательно: сам `alembic/env.py` импортировать
нельзя — на импорте он немедленно запускает миграции, поэтому проверить его
логику тестом невозможно.
"""
from __future__ import annotations

import os
import re

# Сколько ждать блокировку таблицы, прежде чем честно упасть.
#
# ALTER TABLE требует ACCESS EXCLUSIVE, а во время деплоя на Railway старый
# контейнер ещё работает и держит запросы к тем же таблицам. По умолчанию
# PostgreSQL ждёт блокировку БЕСКОНЕЧНО: start.sh не доходит до uvicorn,
# новый контейнер не стартует, деплой висит — и в логах при этом пусто,
# потому что формально ничего не упало. Быстрый честный отказ с понятной
# ошибкой лучше: деплой можно повторить, когда старый контейнер погас.
#
# Ограничивается ТОЛЬКО ожидание блокировки, а не длительность самой
# миграции: долгий бэкфилл данных из-за этого не прервётся.
DEFAULT_LOCK_TIMEOUT = "10s"

# Значение уходит в текст SET-запроса, поэтому принимаем лишь число с
# необязательной единицей измерения — всё остальное отбрасываем.
_LOCK_TIMEOUT_RE = re.compile(r"^\d+(ms|s|min)?$")


def lock_timeout() -> str:
    """MIGRATION_LOCK_TIMEOUT, если он синтаксически безопасен, иначе дефолт."""
    raw = (os.getenv("MIGRATION_LOCK_TIMEOUT") or "").strip()
    return raw if _LOCK_TIMEOUT_RE.match(raw) else DEFAULT_LOCK_TIMEOUT
